package com.cbt.entity;

import java.io.Serializable;

/**
* @ClassName: User 
* @Description: 客户用户信息
 */
public class User implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String userid; //用户id
	private String userName; //用户名
	private String pwd; //密码
	private String tel; //电话
	private String email; //邮箱
	private String email1; //邮箱
	private String loginEmail; //登录邮箱
	private String updateTime; //更新时间
	private String token; //用户登录令牌
	private String createTime; //客户录入时间
	private String companyName; //客户公司
	private String mainBusiness;//主营
	private Integer tempUser;   //临时用户还是正式用户 1：正式用户 2：临时用户

	
	
	public Integer getTempUser() {
		return tempUser;
	}
	public void setTempUser(Integer tempUser) {
		this.tempUser = tempUser;
	}
	public String getMainBusiness() {
		return mainBusiness;
	}
	public void setMainBusiness(String mainBusiness) {
		this.mainBusiness = mainBusiness;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getLoginEmail() {
		return loginEmail;
	}
	public void setLoginEmail(String loginEmail) {
		this.loginEmail = loginEmail;
	}
	public String getEmail1() {
		return email1;
	}
	public void setEmail1(String email1) {
		this.email1 = email1;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(String updateTime) {
		this.updateTime = updateTime;
	}
	
	@Override
	public String toString() {
		return "User [id=" + id + ", userid=" + userid + ", userName="
				+ userName + ", pwd=" + pwd + ", tel=" + tel + ", email="
				+ email + ", email1=" + email1 + ", loginEmail=" + loginEmail
				+ ", updateTime=" + updateTime + ", token=" + token
				+ ", createTime=" + createTime + ", companyName=" + companyName
				+ ", mainBusiness=" + mainBusiness + ", tempUser=" + tempUser
				+ "]";
	}
	
	
}
